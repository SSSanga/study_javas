public class Arrays {
    public static void main(String[] args) {
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
        
        for (int name = 0; name < 4 ; name = name + 1) {
            System.out.print (cars [name] +" " );
        }
        // return 0 ;
        // for문으로 array값 출력하기.     
    }
    
}

